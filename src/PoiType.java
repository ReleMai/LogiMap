/**
 * Point of Interest Type - Categories for notable map locations.
 */
public enum PoiType {
    LANDMARK,    // Natural landmarks (peaks, valleys, etc.)
    STRATEGIC,   // Strategic locations (harbors, passes, etc.)
    RESOURCE,    // Resource-rich areas (forests, mines, etc.)
    SETTLEMENT,  // Player/NPC settlements
    RUINS,       // Ancient ruins or points of historical interest
    OTHER        // Miscellaneous
}
